public class Ej3exception extends Exception {

    /**
     * Creates a new instance of <code>Ej3exception</code> without detail
     * message.
     */
    public Ej3exception() {
    }

    /**
     * Constructs an instance of <code>Ej3exception</code> with the specified
     * detail message.
     *
     * @param msg the detail message.
     */
    public Ej3exception(String msg) {
        super(msg);
    }
}
